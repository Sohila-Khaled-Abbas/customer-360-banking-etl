-- 2. CREATE TABLES
CREATE TABLE SRC_BRANCHES (
    BRANCH_ID   NUMBER PRIMARY KEY,
    BRANCH_NAME VARCHAR2(100) NOT NULL,
    REGION      VARCHAR2(50) NOT NULL CHECK (REGION IN ('CAIRO', 'ALEX', 'DELTA', 'UPPER_EGYPT'))
);

CREATE TABLE SRC_CUSTOMERS (
    CUST_ID        NUMBER PRIMARY KEY,
    CUST_NAME      VARCHAR2(150) NOT NULL,
    CUST_TYPE      VARCHAR2(20) NOT NULL CHECK (CUST_TYPE IN ('VIP', 'NORMAL')),
    BLACKLIST_FLAG VARCHAR2(1) DEFAULT 'N' NOT NULL CHECK (BLACKLIST_FLAG IN ('Y', 'N'))
);

CREATE TABLE SRC_ACCOUNTS (
    ACC_ID     NUMBER PRIMARY KEY,
    CUST_ID    NUMBER NOT NULL,
    BRANCH_ID  NUMBER NOT NULL,
    ACC_TYPE   VARCHAR2(30) NOT NULL CHECK (ACC_TYPE IN ('SAVINGS', 'CURRENT')),
    ACC_STATUS VARCHAR2(20) NOT NULL CHECK (ACC_STATUS IN ('ACTIVE', 'SUSPENDED')),
    CONSTRAINT fk_accounts_cust FOREIGN KEY (CUST_ID) REFERENCES SRC_CUSTOMERS(CUST_ID),
    CONSTRAINT fk_accounts_branch FOREIGN KEY (BRANCH_ID) REFERENCES SRC_BRANCHES(BRANCH_ID)
);

